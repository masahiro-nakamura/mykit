<template>
  <panel title="Search">
    <v-layout>
      <v-text-field
        label="Search by song title, artist, ganre or album"
        v-model="search">
      </v-text-field>
    </v-layout>
  </panel>
</template>

<script>
export default {
  data () {
    return {
      search: ''
    }
  },
  watch: {
    search (value) {
      const route = {
        name: 'songs'
      }
      if (this.search !== '') {
        route.query = {
          search: this.search
        }
      }
      this.$router.push(route)
    },
    '$route.query.search': {
      immediate: true,
      handler (value) {
        this.search = value
      }
    }
  }
}
</script>

